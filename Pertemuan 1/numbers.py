#3 tipe data numerik pada python

x = 9    # int
y = 8.7  # float
z = 6j   # complex

print(type(x))
print(type(y))
print(type(z))

#konversi tipe
#convert dari int ke float:
a = float(x)

#convert dari float ke int:
b = int(y)

#convert dari int ke complex:
c = complex(x)

print(a)
print(b)
print(c)

print(type(a))
print(type(b))
print(type(c))