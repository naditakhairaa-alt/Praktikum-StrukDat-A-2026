#menentukan tipe variable

#integer
x = int(5)   # x akan menjadi 5
y = int(7.5) # y akan menjadi 7
z = int("10") # z akan menjadi 10
print(x)
print(y)
print(z)

#float
x = float(88)     # x akan menjadi 88.0
x = float(88)     # x akan menjadi 88.0
y = float(9.8)   # y akan menjadi 9.8
z = float("8")   # z akan menjadi 8.0
w = float("8.8") # w akan menjadi 8.8
print(x)
print(y)
print(z)
print(w)

#string
x = str("A1") # x akan menjadi 'A1'
y = str(5)    # y akan menjadi '5'
z = str(0.9)  # z akan menjadi '0.9'
print(x)
print(y)
print(z)